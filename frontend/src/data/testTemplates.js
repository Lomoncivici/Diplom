export function getTemplateById(templateId) {
  return testTemplates.find((template) => template.id === templateId) || null
}

export function buildPayloadFromTemplate(templateId) {
  const template = getTemplateById(templateId) || testTemplates[0]
  return JSON.parse(JSON.stringify(template.payload))
}
